let resultado;

// Redondear

resultado = Math.round(5.4);
console.log(resultado);

// Redondear hacia arriba

resultado = Math.ceil(2.1);
console.log(resultado);

// Redondear hacia abajo

resultado = Math.floor(8.9);
console.log(resultado);

// Raiz cuadrada

resultado = Math.sqrt(144);
console.log(resultado);

// Absoluto

resultado = Math.abs(-500);
console.log(resultado);

// Potencia

resultado = Math.pow(2, 5);
console.log(resultado);

// Mínimo

resultado = Math.min( 1, 2, 3, 4);
console.log(resultado);

// Máximo

resultado = Math.max(1, 2, 3, 4, 5);
console.log(resultado);

// Aleatorio

resultado = Math.random() * 2;
console.log(resultado);

// Aleatorio dentro de un rango

resultado = Math.floor(Math .random() * 3);
console.log(resultado);